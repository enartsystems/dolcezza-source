
 CREATE TABLE IF NOT EXISTS ROL(
ALTA DATE NOT NULL ,
ROL VARCHAR(100) NOT NULL  UNIQUE ,
IDXROL BIGSERIAL PRIMARY KEY ,
IDROLPARENT0 INT4
);
 CREATE TABLE IF NOT EXISTS USUARIO(
LASTLOGIN TIMESTAMP WITHOUT TIME ZONE NOT NULL ,
PASSWORD VARCHAR(1000) NOT NULL ,
ALTA DATE,
LASTNAME VARCHAR(100),
FIRSTNAME VARCHAR(100),
EMAIL VARCHAR(100) NOT NULL  UNIQUE ,
USERNAME VARCHAR(25) NOT NULL  UNIQUE ,
IDXUSUARIO BIGSERIAL PRIMARY KEY ,
LOCKED BOOL NOT NULL 
);
CREATE TABLE IF NOT EXISTS RUSERROL(
IDXRUSERROL BIGSERIAL PRIMARY KEY ,
IDROL0 INT4 NOT NULL ,
IDUSUARIO0 INT4 NOT NULL 
);

